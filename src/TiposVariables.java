public class TiposVariables {
    public static void main(String[] args) {
        // Datos Enteros
        int edad = 28;
        System.out.println(edad);

        edad = 32;
        System.out.println(edad);

        edad = 32 + 28;
        System.out.println("Mi edad es " + edad);

        // Datos Decimales
        double salario = 1250.56;
        System.out.println(salario);

        double edad2 = 28;
        System.out.println(edad2);

        double salarioMitad = salario / 2;
        System.out.println(salarioMitad);

        int division = 1250 / 3;
        System.out.println(division);

        // Conversiones
        double variable1 = 230.89;
        int variable1Entero = (int) variable1;
        System.out.println(variable1Entero);

        // Otros tipos de datos
        long prueba = 122222222222433333L;
        short numeroPequeño = 13555;
        byte numeroAunMasPequeño = 15;
        float numeroDecimalPequeño = 2.5F;

    }
}
